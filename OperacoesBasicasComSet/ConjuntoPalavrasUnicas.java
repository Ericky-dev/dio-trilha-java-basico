package OperacoesBasicasComSet;

public class ConjuntoPalavrasUnicas {
	
   //atributos;
	
	String palavrasUnicas;

	public ConjuntoPalavrasUnicas(String palavrasUnicas) {
	
		this.palavrasUnicas = palavrasUnicas;
	}

	public String getPalavrasUnicas() {
		return palavrasUnicas;
	}

	public void setPalavrasUnicas(String palavrasUnicas) {
		this.palavrasUnicas = palavrasUnicas;
	}

	@Override
	public String toString() {
		return "ConjuntoPalavrasUnicas [palavrasUnicas=" + palavrasUnicas + "]";
	}
	
	
	
	

}
